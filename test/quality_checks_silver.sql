 Purpose         : Performs post-load data quality validations on curated 
                   'silver' schema tables to ensure accuracy, completeness, 
                   and consistency.

 Description     : 
    This script runs essential QA checks after the ETL process, including:
        - Null or duplicate primary keys
        - Data standardization across categorical fields
        - Detection of unwanted trailing/leading spaces
        - Logical date sequence validations (e.g., order date < ship date)
        - Integrity checks between related fields (e.g., sales = quantity × price)

 Usage Notes     : 
    - Execute after each full load of the silver layer
    - Investigate and resolve any rows returned from the queries
    - This script is read-only; it does not modify data

 Parameters      : None

 Returns         : Query result sets highlighting potential data issues

